require_tools() {
  missing=''
  for t in "$@"; do
    have "$t" || missing="$missing $t"
  done
  [ -z "$missing" ] || err "Missing required tools:$missing"
}

# Download a URL to a file over enforced HTTPS/TLS 1.2, with retries.
# The live progress bar is shown only when stderr is an interactive terminal and
# SLICER_QUIET is unset.
# When output is piped, redirected, or logged, the terminal never collapses the
# bar's carriage returns, so each update would append instead of overwrite and
# flood the console with '#' characters (burying real error messages). In that
# case we stay quiet but keep error reporting via --show-error / no -q suppression.
download() {
  if have curl; then
    if [ -t 2 ] && [ -z "$SLICER_QUIET" ]; then progress='--progress-bar'; else progress='--silent --show-error'; fi
    # shellcheck disable=SC2086
    curl --fail --location --proto '=https' --tlsv1.2 \
         --retry 3 --retry-delay 2 --retry-connrefused \
         $progress --output "$2" "$1"
  elif have wget; then
    if [ -t 2 ] && [ -z "$SLICER_QUIET" ]; then progress='--show-progress'; else progress=''; fi
    # shellcheck disable=SC2086
    wget --https-only --secure-protocol=TLSv1_2 --tries=3 --timeout=30 \
         $progress -qO "$2" "$1"
  else
    err "Neither curl nor wget is available; cannot download Slicer."
  fi
}

# Fetch a URL to stdout (for JSON metadata), same TLS hardening.
fetch() {
  if have curl; then
    curl --fail --silent --show-error --location --proto '=https' --tlsv1.2 "$1"
  elif have wget; then
    wget --https-only --secure-protocol=TLSv1_2 -qO- "$1"
  fi
}

# Resolve the Girder item id backing a download endpoint (needs curl).
resolve_item_id() {
  have curl || { echo ''; return 0; }
  redir=$(curl --fail --silent --proto '=https' --tlsv1.2 \
               -o /dev/null -w '%{redirect_url}' "$1" 2>/dev/null || true)
  case "$redir" in
    */bitstream/*) basename "$redir" ;;
    *) echo '' ;;
  esac
}

# Extract the string value of a JSON field from the metadata blob $1 ($2 = key).
# Prints nothing when the key is absent.
json_field() {
  printf '%s' "$1" \
    | grep -oE "\"$2\"[[:space:]]*:[[:space:]]*\"[^\"]*\"" \
    | head -n1 \
    | sed 's/^[^:]*:[[:space:]]*"//; s/"$//'
}

# Ask the download server which package it would serve for <os>, then read that
# package's metadata: its checksum, version and architecture. Knowing the
# version and architecture up front is what lets us tell whether this exact
# build is already installed without downloading half a gigabyte to find out.
resolve_package() {
  os="$1"
  # The download server speaks release/nightly/any; map our user-facing names.
  case "$SLICER_RELEASE_TYPE" in
    stable)  stability=release ;;
    preview) stability=nightly ;;
    any)     stability=any ;;
    *) err "SLICER_RELEASE_TYPE must be 'stable', 'preview' or 'any' (got '$SLICER_RELEASE_TYPE')." ;;
  esac
  PKG_URL="$BASE_URL?os=$os&stability=$stability"
  [ -n "$SLICER_VERSION" ] && PKG_URL="$PKG_URL&version=$SLICER_VERSION"

  PKG_ITEM_ID=$(resolve_item_id "$PKG_URL")
  PKG_SHA='' PKG_VERSION='' PKG_ARCH=''
  [ -n "$PKG_ITEM_ID" ] || return 0

  meta=$(fetch "$PACKAGES_API/item/$PKG_ITEM_ID" 2>/dev/null || true)
  PKG_SHA=$(json_field "$meta" sha512)
  PKG_VERSION=$(json_field "$meta" version)
  PKG_ARCH=$(json_field "$meta" arch)

  # A digest we can't recognize is worse than no digest at all: it would fail
  # every comparison and abort an otherwise sound download.
  case "$PKG_SHA" in *[!a-f0-9]*) PKG_SHA='' ;; esac
  [ "${#PKG_SHA}" -eq 128 ] || PKG_SHA=''

  PKG_URL="$PACKAGES_API/item/$PKG_ITEM_ID/download"
}

# Compute the SHA-512 of a file, printing nothing when no tool is available.
sha512_of() {
  if have sha512sum; then
    sha512sum "$1" | awk '{print $1}'
  elif have shasum; then
    shasum -a 512 "$1" | awk '{print $1}'
  fi
}

verify_sha512() {
  # verify_sha512 <file> <expected-hex>
  actual=$(sha512_of "$1")
  if [ -z "$actual" ]; then
    warn "No SHA-512 tool found; skipping checksum verification."
    return 0
  fi
  [ "$actual" = "$2" ] || err "Checksum verification failed (expected $2, got $actual)."
  log "Checksum verified (SHA-512)."
}

# True when <file> exists and already matches <expected-hex>, i.e. a previously
# downloaded package we can trust and reuse without fetching it again.
cached_download_valid() {
  [ -f "$1" ] || return 1
  actual=$(sha512_of "$1")
  [ -n "$actual" ] && [ "$actual" = "$2" ]
}

# Download the package resolved by resolve_package(), verifying its checksum when
# possible and reusing a previously downloaded copy from the cache when it still
# matches. $1 is the OS token (linux|macosx); $2 is a throwaway path used only
# when the cache is unusable. Sets the global DL_PKG to the verified package.
download_verified() {
  os="$1"; tmp_out="$2"

  case "$os" in
    linux)  ext='tar.gz' ;;
    macosx) ext='dmg' ;;
    *)      ext='bin' ;;
  esac

  # Cache downloads between runs, keyed by version, so re-running the installer
  # reuses an already-fetched package instead of downloading it again. Without a
  # resolved version we can't name the file stably, so caching is skipped.
  cache_file=''
  cache_dir="${XDG_CACHE_HOME:-$HOME/.cache}/slicer-installer"
  if [ -n "$PKG_VERSION" ] && mkdir -p "$cache_dir" 2>/dev/null && [ -w "$cache_dir" ]; then
    cache_file="$cache_dir/Slicer-$PKG_VERSION-$os.$ext"
  fi

  # Reuse the cached package when it is present and still passes verification.
  if [ -n "$cache_file" ] && [ -n "$PKG_SHA" ] && cached_download_valid "$cache_file" "$PKG_SHA"; then
    log "Using cached 3D Slicer $PKG_VERSION ($os)."
    DL_PKG="$cache_file"
    return 0
  fi

  # Download into the cache when we have a stable name for it (so the next run
  # can reuse it), otherwise into the throwaway temp path.
  out="${cache_file:-$tmp_out}"
  if [ -n "$PKG_VERSION" ]; then
    log "Downloading 3D Slicer $PKG_VERSION ($os)…"
  else
    log "Downloading 3D Slicer ($os)…"
  fi
  download "$PKG_URL" "$out"

  if [ -n "$PKG_SHA" ]; then
    verify_sha512 "$out" "$PKG_SHA"
  else
    warn "Could not obtain a checksum from the server; skipping verification."
  fi
  DL_PKG="$out"
}

# ---------------------------------------------------------------------------- #
# existing installations
# ---------------------------------------------------------------------------- #
# True when there is a terminal to prompt on. Under `curl | sh` stdin is the
# script itself, so /dev/tty is the only way to reach the user — and it cannot be
# opened at all in CI, in containers, or under `sh -c`, where asking would hang.
# `true` rather than `:`, because a failed redirect on a special builtin exits.
tty_available() { { true < /dev/tty; } 2>/dev/null; }

# Print the prompt $1 on the terminal and read one line into ANSWER.
# Returns 1 at end of input, e.g. when the user presses Ctrl-D.
ask() {
  printf '%s' "$1" > /dev/tty
  read -r ANSWER < /dev/tty || return 1
}

# Ask for another directory to install into and point DEST at it. A leading ~ is
# expanded, and directories we cannot write to are rejected up front: this
# installer never asks for root, so it cannot recover from that later.
prompt_install_dir() {
  while true; do
    ask 'Install directory: ' || { printf '\n' > /dev/tty; err "No directory given."; }
    case "$ANSWER" in
      '')    printf '  Please enter a path.\n' > /dev/tty; continue ;;
      '~')   ANSWER="$HOME" ;;
      '~/'*) ANSWER="$HOME/${ANSWER#\~/}" ;;
    esac
    if ! mkdir -p "$ANSWER" 2>/dev/null || [ ! -w "$ANSWER" ]; then
      printf '  Cannot write to %s — pick another directory.\n' "$ANSWER" > /dev/tty
      continue
    fi
    DEST="$ANSWER"
    log "Installing into $DEST instead."
    return 0
  done
}

# An installation we would destroy already occupies $2 ($1 names it for the
# user). Decide what to do about it. $3 describes what replacing it would mean,
# and defaults to reinstalling the very same version; macOS passes its own text,
# because there any version installs over any other. $4 names the shell function
# that removes an installation given its path, invoked by the "Uninstall" choice.
#
# Returns 0 when the caller should go ahead and install into the current DEST,
# and 1 once DEST points at a directory the user picked instead — which the
# caller must re-check, since that directory may be occupied too.
# Never returns when the user aborts or uninstalls.
resolve_existing_install() {
  label="$1"; occupied="$2"
  replace_desc="${3:-Reinstall — replace it with a freshly downloaded copy}"
  uninstall_fn="${4:-}"

  case "$SLICER_IF_EXISTING" in
    abort)
      log "$label is already installed at $occupied; nothing to do."
      exit 0 ;;
    reinstall)
      log "$label is already installed at $occupied; replacing it."
      return 0 ;;
    uninstall)
      log "$label is already installed at $occupied; uninstalling it."
      "$uninstall_fn" "$occupied"
      log "${C_GREEN}3D Slicer has been uninstalled.${C_RESET}"
      exit 0 ;;
    prompt) ;;
    *) err "SLICER_IF_EXISTING must be 'prompt', 'abort', 'reinstall' or 'uninstall' (got '$SLICER_IF_EXISTING')." ;;
  esac

  if [ -n "$SLICER_NONINTERACTIVE" ]; then
    log "$label is already installed at $occupied; reinstalling (non-interactive mode)."
    return 0
  fi

  if ! tty_available; then
    warn "$label is already installed at $occupied, and there is no terminal to ask"
    warn "what to do about it. Replacing it. Set SLICER_IF_EXISTING=abort to skip."
    return 0
  fi

  {
    printf '\n%sWARN:%s %s is already installed at:\n' "$C_YELLOW" "$C_RESET" "$label"
    printf '        %s\n\n' "$occupied"
    printf '  1) Abort — leave the existing installation untouched\n'
    printf '  2) %s\n' "$replace_desc"
    printf '  3) Install elsewhere — keep both, in a directory you choose\n'
    printf '  4) Uninstall — remove the existing installation and exit\n\n'
  } > /dev/tty

  while true; do
    ask 'Choose [1-4] (default 1): ' || { printf '\n' > /dev/tty; ANSWER=1; }
    case "$ANSWER" in
      ''|1) log "Aborted; $occupied was left untouched."; exit 0 ;;
      2)    log "Replacing $occupied."; return 0 ;;
      3)    prompt_install_dir; return 1 ;;
      4)    "$uninstall_fn" "$occupied"
            log "${C_GREEN}3D Slicer has been uninstalled.${C_RESET}"; exit 0 ;;
      *)    printf '  Please answer 1, 2, 3 or 4.\n' > /dev/tty ;;
    esac
  done
}

# ---------------------------------------------------------------------------- #
# Linux
# ---------------------------------------------------------------------------- #
# Run one package-manager command line during an auto dependency install,
# echoing it first so the (Docker) build log records exactly what ran. $1 is a
# command line we assembled ourselves from fixed tokens, so the deliberate word
# splitting is safe. Any failure is fatal — checked explicitly with err() rather
# than left to `set -e`, whose handling of a function called from an `&&` list is
# too shell-dependent to trust: a failed dependency install must fail the whole
# install, so a Docker build stops instead of baking a broken image that silently
# lacks the libraries.
run_deps_cmd() {
  log "Running: $1"
  # shellcheck disable=SC2086
  $1 || err "Dependency installation failed (command: $1)."
}

# Slicer links against a handful of system libraries that, by default, we
# deliberately do NOT install. Doing so needs root, and asking a `curl | sh`
# script for sudo is a far bigger ask than the install itself — the whole point
# is that this script only ever writes under $HOME. So print the command for the
# detected package manager and let the user decide whether to run it.
#
# Setting SLICER_INSTALL_DEPS opts into running that command instead of printing
# it, using sudo when not already root: the mode for building Docker images and
# other automation where root is available and installing system packages is the
# whole point.
handle_linux_deps() {
  auto="$SLICER_INSTALL_DEPS"

  # Quiet mode suppresses the printed hint: its prose goes through log(), but the
  # install command is a bare printf, so a half-shown hint would be worse. It
  # never suppresses an actual auto-install — that still has to happen.
  [ -n "$auto" ] || [ -z "$SLICER_QUIET" ] || return 0

  if [ "$(id -u)" -eq 0 ]; then sudo_prefix=''; else sudo_prefix='sudo '; fi

  if have apt-get; then
    # A fresh container image ships with empty package lists, which would both
    # fail the install and mislead the libasound2t64 probe below into choosing
    # the wrong ALSA package. When auto-installing, refresh them first; in hint
    # mode we only read local lists (apt-cache), which is side-effect free.
    [ -n "$auto" ] && run_deps_cmd "${sudo_prefix}apt-get update"

    # Common Debian/Ubuntu runtime libraries required by Slicer.
    pkgs="libglu1-mesa libpulse-mainloop-glib0 libnss3 qt5dxcb-plugin libsm6"
    # ALSA lib was renamed during the 64-bit time_t transition (Ubuntu 24.04+).
    if apt-cache show libasound2t64 >/dev/null 2>&1; then
      pkgs="$pkgs libasound2t64"
    else
      pkgs="$pkgs libasound2"
    fi
    cmd="${sudo_prefix}apt-get install -y $pkgs"
  elif have dnf; then
    pkgs="mesa-libGLU mesa-libGL libnsl libXrender pulseaudio-libs-glib2 nss"
    pkgs="$pkgs libXcomposite libXdamage libXrandr ftgl libXcursor libXi libXtst"
    pkgs="$pkgs alsa-lib qt5-qtx11extras"
    cmd="${sudo_prefix}dnf install -y $pkgs"
  elif have pacman; then
    pkgs="glu nss alsa-lib libxrender libxcomposite libxdamage libxrandr"
    pkgs="$pkgs libxcursor libxi libxtst ftgl"
    # --noconfirm only when auto-installing, so an unattended build never blocks
    # on a prompt; the printed hint keeps the interactive default for humans.
    if [ -n "$auto" ]; then confirm='--noconfirm '; else confirm=''; fi
    cmd="${sudo_prefix}pacman -S --needed ${confirm}$pkgs"
  else
    if [ -n "$auto" ]; then
      err "SLICER_INSTALL_DEPS is set but no supported package manager (apt-get, dnf, pacman) was found; install Slicer's runtime libraries manually."
    fi
    warn "Unrecognized package manager. If Slicer fails to start, install its"
    warn "runtime libraries manually (see the Slicer docs)."
    return 0
  fi

  if [ -n "$auto" ]; then
    run_deps_cmd "$cmd"
    log "${C_GREEN}Slicer's runtime system libraries are installed.${C_RESET}"
    return 0
  fi

  printf '\n'
  log "Slicer needs a few system libraries. Most desktops already have them; if"
  log "Slicer fails to start, install them with:"
  printf '\n    %s%s%s\n' "$C_GREEN" "$cmd" "$C_RESET"

  if have pacman; then
    printf '\n'
    log "Arch Linux: a prebuilt AUR package (3dslicer-bin) is available as an alternative."
  fi
}

# Remove the Linux installation at $1 (a Slicer-<version>-linux-<arch> directory):
# the directory itself, the launcher and "current version" symlinks that point
# into it, and the desktop entry when it still launches this copy. Anything that
# points at a different install is deliberately left alone.
uninstall_linux() {
  target="$1"
  bindir="$HOME/.local/bin"
  parent="$(dirname "$target")"
  apps_dir="$HOME/.local/share/applications"
  desktop_file="$apps_dir/slicer.desktop"

  rm -rf "$target"

  # Drop the launcher and "current version" symlinks only when they still resolve
  # to the copy we just removed, so links belonging to another install survive.
  if [ "$(readlink "$bindir/Slicer" 2>/dev/null)" = "$target/Slicer" ]; then
    rm -f "$bindir/Slicer"
  fi
  if [ "$(readlink "$parent/Slicer" 2>/dev/null)" = "$target" ]; then
    rm -f "$parent/Slicer"
  fi

  # Remove the desktop entry when its Exec line still launches this copy.
  if [ -f "$desktop_file" ] && grep -qF "Exec=$target/Slicer " "$desktop_file"; then
    rm -f "$desktop_file"
    if have update-desktop-database; then
      update-desktop-database "$apps_dir" >/dev/null 2>&1 || true
    fi
  fi

  log "Removed $target"
}

# Add an XDG desktop menu entry so Slicer appears in application launchers.
install_desktop_entry() {
  appdir="$1"
  apps_dir="$HOME/.local/share/applications"
  desktop_file="$apps_dir/slicer.desktop"
  mkdir -p "$apps_dir"

  # Prefer Slicer's official desktop icon. It lives deep in the tree
  # (lib/Slicer-<ver>/qt-scripted-modules/Resources/docroot/images) and is named
  # 3DSlicer-DesktopIcon.png, so neither the old maxdepth nor the slicer*.png glob
  # would ever reach it. Fall back to any slicer*.png, then to the Slicer binary.
  icon=$(find "$appdir" -type f -name '3DSlicer-DesktopIcon.png' 2>/dev/null | head -n1 || true)
  [ -n "$icon" ] || icon=$(find "$appdir" -maxdepth 3 -iname 'slicer*.png' 2>/dev/null | head -n1 || true)
  [ -n "$icon" ] || icon="$appdir/Slicer"

  cat > "$desktop_file" <<EOF
[Desktop Entry]
Type=Application
Name=3D Slicer
GenericName=Medical Image Computing
Comment=Analyze and visualize medical image data
Exec=$appdir/Slicer %F
Icon=$icon
Terminal=false
Categories=Graphics;Science;Education;
Keywords=medical;imaging;DICOM;visualization;segmentation;
EOF

  if have update-desktop-database; then
    update-desktop-database "$apps_dir" >/dev/null 2>&1 || true
  fi
  log "Desktop menu entry created: $desktop_file"
}

install_linux() {
  require_tools tar

  DEST="${SLICER_INSTALL_DIR:-$HOME/.local/opt}"
  BINDIR="$HOME/.local/bin"

  resolve_package linux

  # Every Linux package extracts to a directory named Slicer-<version>-linux-<arch>,
  # so the metadata tells us where this build would land. Settle what to do about
  # an existing copy of it now, before spending a download on it.
  pkgdir=''
  if [ -n "$PKG_VERSION" ] && [ -n "$PKG_ARCH" ]; then
    pkgdir="Slicer-$PKG_VERSION-linux-$PKG_ARCH"
    while [ -d "$DEST/$pkgdir" ]; do
      if resolve_existing_install "3D Slicer $PKG_VERSION" "$DEST/$pkgdir" '' uninstall_linux; then break; fi
    done
  fi

  TMP="$(mktemp -d)"
  trap 'rm -rf "$TMP"' EXIT INT TERM

  download_verified linux "$TMP/slicer.tar.gz"

  log "Extracting archive…"
  mkdir -p "$TMP/x"
  tar -xzf "$DL_PKG" -C "$TMP/x"

  srcdir="$(find "$TMP/x" -maxdepth 1 -type d -name 'Slicer-*' | head -n1)"
  [ -n "$srcdir" ] || err "Could not locate the extracted Slicer directory."

  appdir="$DEST/$(basename "$srcdir")"

  # Reached only when the server's metadata was unavailable, so we could not name
  # the directory in advance. The archive has just named it for us; ask now
  # rather than silently deleting whatever is sitting there.
  if [ -z "$pkgdir" ]; then
    while [ -d "$appdir" ]; do
      if resolve_existing_install "3D Slicer" "$appdir" '' uninstall_linux; then break; fi
      appdir="$DEST/$(basename "$srcdir")"
    done
  fi

  mkdir -p "$DEST"
  rm -rf "$appdir"
  mv "$srcdir" "$appdir"

  mkdir -p "$BINDIR"
  ln -sfn "$appdir/Slicer" "$BINDIR/Slicer"   # launcher on PATH
  ln -sfn "$appdir" "$DEST/Slicer"            # stable "current version" link

  install_desktop_entry "$appdir"

  log "Installed to: $appdir"
  log "Launcher    : $BINDIR/Slicer"
  case ":$PATH:" in
    *":$BINDIR:"*) log "Run it with: ${C_GREEN}Slicer${C_RESET} (or from your app menu)." ;;
    *) warn "$BINDIR is not on your PATH. Run it with: $BINDIR/Slicer (or add that dir to PATH)." ;;
  esac
}

# ---------------------------------------------------------------------------- #
# macOS
# ---------------------------------------------------------------------------- #
# Print the version of the Slicer.app bundle at $1, or nothing when no bundle is
# there and when its Info.plist cannot be read.
macos_app_version() {
  [ -f "$1/Contents/Info.plist" ] || return 0
  /usr/bin/defaults read "$1/Contents/Info" CFBundleShortVersionString 2>/dev/null || true
}

# Remove the Slicer.app bundle at $1, stopping a running instance first so the
# bundle can be deleted cleanly.
uninstall_macos() {
  target="$1"
  if /usr/bin/pgrep -x Slicer >/dev/null 2>&1; then
    log "Stopping running Slicer instance…"
    /usr/bin/pkill -x Slicer 2>/dev/null || true
    sleep 1
  fi
  rm -rf "$target"
  log "Removed $target"
}

install_macos() {
  require_tools hdiutil

  # ~/Applications, not /Applications: the latter is root:admin, so writing to it
  # would need sudo for any standard or MDM-managed user. LaunchServices indexes
  # both, so Launchpad, Spotlight and `open -a` find the app either way.
  DEST="${SLICER_INSTALL_DIR:-$HOME/Applications}"

  # The download server ships an Intel (amd64) build; it runs natively on Intel
  # Macs and via Rosetta 2 on Apple Silicon.
  if [ "$(uname -m)" = "arm64" ] && ! /usr/bin/pgrep -q oahd 2>/dev/null; then
    warn "Apple Silicon detected. Slicer's Intel build needs Rosetta 2."
    warn "If Slicer won't launch, install it with: softwareupdate --install-rosetta --agree-to-license"
  fi

  resolve_package macosx

  # Slicer.app carries no version in its name, so whatever bundle sits at the
  # destination gets overwritten — upgrading discards the old version just as
  # surely as reinstalling the same one does. Ask about either, before
  # downloading, and let "install elsewhere" keep the two side by side.
  while [ -e "$DEST/Slicer.app" ]; do
    installed="$(macos_app_version "$DEST/Slicer.app")"

    if [ -n "$installed" ]; then label="3D Slicer $installed"; else label="3D Slicer"; fi

    if [ -n "$installed" ] && [ "$installed" = "$PKG_VERSION" ]; then
      replace=''  # same version; the default "Reinstall" wording says it best
    elif [ -n "$PKG_VERSION" ]; then
      replace="Replace — discard it and install 3D Slicer $PKG_VERSION"
    else
      replace="Replace — discard it and install the version being downloaded"
    fi

    if resolve_existing_install "$label" "$DEST/Slicer.app" "$replace" uninstall_macos; then break; fi
  done

  # Stop any running instance so the bundle can be replaced cleanly.
  if /usr/bin/pgrep -x Slicer >/dev/null 2>&1; then
    log "Stopping running Slicer instance…"
    /usr/bin/pkill -x Slicer 2>/dev/null || true
    sleep 1
  fi

  mkdir -p "$DEST" || err "Could not create $DEST."
  # Fail loudly here rather than half-way through the copy. Only reachable when
  # SLICER_INSTALL_DIR points somewhere root-owned; the default is under $HOME.
  [ -w "$DEST" ] || err "$DEST is not writable. Set SLICER_INSTALL_DIR to a directory you own."

  TMP="$(mktemp -d)"
  MNT="$TMP/mnt"
  cleanup_macos() {
    hdiutil detach "$MNT" -quiet >/dev/null 2>&1 || true
    rm -rf "$TMP"
  }
  trap cleanup_macos EXIT INT TERM

  download_verified macosx "$TMP/Slicer.dmg"

  log "Mounting disk image…"
  mkdir -p "$MNT"
  hdiutil attach "$DL_PKG" -nobrowse -quiet -mountpoint "$MNT"

  app="$(/usr/bin/find "$MNT" -maxdepth 1 -name '*.app' | head -n1)"
  [ -n "$app" ] || err "No .app bundle found inside the disk image."

  appname="$(basename "$app")"
  dest="$DEST/$appname"
  log "Installing $appname to $DEST…"
  rm -rf "$dest" || err "Could not remove the existing $dest. Remove it manually and re-run."
  cp -R "$app" "$DEST/"

  # Best-effort: clear the quarantine flag so Gatekeeper lets it open.
  # curl and wget don't set com.apple.quarantine, so this is usually a no-op.
  xattr -dr com.apple.quarantine "$dest" 2>/dev/null || true

  log "Installed to: $dest"

  # A copy in /Applications shadows this one: `open -a Slicer` resolves through
  # LaunchServices by bundle id, and may well pick the other install.
  sys_app="/Applications/$appname"
  if [ "$dest" != "$sys_app" ] && [ -e "$sys_app" ]; then
    warn "Another copy exists at $sys_app; 'open -a Slicer' may launch that one instead."
    warn "Remove it (sudo rm -rf '$sys_app') or open this one directly: open '$dest'"
  fi

  log "Launch it from Launchpad/Spotlight, or run: ${C_GREEN}open -a Slicer${C_RESET}"
}

# ---------------------------------------------------------------------------- #
# main
# ---------------------------------------------------------------------------- #
main() {
  setup_colors
  print_logo
  have curl || have wget || err "This installer requires 'curl' or 'wget'."

  OS="$(uname -s)"
  case "$OS" in
    Linux)  install_linux ;;
    Darwin) install_macos ;;
    *) err "Unsupported operating system: $OS (this script handles Linux and macOS)." ;;
  esac

  log "${C_GREEN}3D Slicer installation complete.${C_RESET}"

  # Left for last so the one thing that may still need the user's attention —
  # and their sudo password — is the last thing they read (or, with
  # SLICER_INSTALL_DEPS, the last thing that runs).
  if [ "$OS" = Linux ]; then handle_linux_deps; fi
}

main "$@"
