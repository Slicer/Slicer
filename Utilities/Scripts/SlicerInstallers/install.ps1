<#
    3D Slicer one-line installer for Windows.

    Usage (PowerShell or cmd):
        powershell -c "irm https://raw.githubusercontent.com/mauigna06/slicer-installer/main/install.ps1 | iex"

    What it does:
        * Downloads the latest STABLE Slicer installer from the official server.
        * Verifies it against the publisher's SHA-512 checksum.
        * Runs it silently, per-user (no Administrator rights required).
        * All of Slicer's dependencies (Qt, Python, VTK, ...) are bundled in the
          installer, so nothing else needs to be installed on Windows.

    Each version installs into its own directory, so different versions live side
    by side. When the version being installed is already installed, you are asked
    whether to abort, reinstall over it, install it into another directory, or
    uninstall the existing copy.

    Environment overrides:
        SLICER_RELEASE_TYPE  stable (default) | preview | any
        SLICER_VERSION       pin an exact version, e.g. 5.12.0 (default: latest)
        SLICER_INSTALL_DIR   install directory (default: the installer's default,
                             %LOCALAPPDATA%\NA-MIC). Use an ASCII-only path.
        SLICER_IF_EXISTING   what to do when that version is already installed:
                             prompt (default) | abort | reinstall | uninstall
        SLICER_NONINTERACTIVE  set (to any value) to never prompt, for automations
                             and CI. If the target version is already installed it
                             is reinstalled, unless SLICER_IF_EXISTING is set to
                             abort or uninstall. Every other override still applies
                             - in particular SLICER_VERSION to pin the version.
        SLICER_QUIET         set to silence progress messages, the logo and the
                             download bar; warnings, errors and prompts still show
        NO_COLOR             set to disable colored output

    Docs: https://slicer.readthedocs.io/en/latest/user_guide/getting_started.html
#>

