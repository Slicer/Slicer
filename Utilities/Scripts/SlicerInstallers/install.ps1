# Resolve the Girder item id behind the download endpoint so we can fetch the
# publisher's checksum. The endpoint answers with a 302 to a .../bitstream/<id>
# URL; we read that Location without following it.
#
# We use HttpWebRequest with AllowAutoRedirect disabled (as Save-UrlWithProgress
# does) rather than `Invoke-WebRequest -MaximumRedirection 0`: on Windows
# PowerShell 5.1 the latter *throws* on a 3xx, and the resulting exception has no
# .Response property, so introspecting it under Set-StrictMode aborts the script.
# GetResponse() instead returns 3xx responses (only 4xx/5xx throw), giving one
# code path that behaves the same on Windows PowerShell 5.1 and PowerShell 7.
# Best-effort: on any failure we fall back to an unverified download rather than
# blocking the install.
function Resolve-SlicerItemId {
    param([string]$Url)
    $loc = $null
    try {
        $req = [Net.HttpWebRequest]::Create($Url)
        $req.AllowAutoRedirect = $false
        $req.UserAgent = 'slicer-installer (PowerShell)'
        $resp = $req.GetResponse()
        try { $loc = [string]$resp.Headers['Location'] } finally { $resp.Dispose() }
    } catch {
        $loc = $null
    }
    if ($loc -match '/bitstream/([0-9a-fA-F]+)') { return $Matches[1] }
    return $null
}

# Fetch the item's metadata once and read both the checksum and the version from
# it, so a single round-trip backs both values (install.sh likewise fetches the
# blob once). Property access goes through PSObject.Properties because
# Set-StrictMode turns a missing field into a terminating error rather than $null.
#
# A digest we cannot recognize is worse than no digest at all: it would fail every
# comparison and abort an otherwise sound download. So a checksum that is not
# exactly 128 hex characters (a SHA-512) is discarded here, degrading to an
# unverified download, exactly as install.sh does. Best-effort throughout: any
# failure leaves the field $null.
function Get-SlicerMeta {
    param([string]$ItemId)
    $result = [pscustomobject]@{ Sha512 = $null; Version = $null }
    try {
        $item = Invoke-RestMethod -Uri "$packagesApi/item/$ItemId" -UseBasicParsing
        if ($item.PSObject.Properties['meta'] -and $item.meta) {
            $m = $item.meta
            if ($m.PSObject.Properties['sha512']) {
                $sha = ([string]$m.sha512).Trim().ToLower()
                if ($sha -match '^[0-9a-f]{128}$') { $result.Sha512 = $sha }
            }
            if ($m.PSObject.Properties['version']) {
                $result.Version = [string]$m.version
            }
        }
    } catch { }
    return $result
}

# ---------------------------------------------------------------------------- #
# existing installations
# ---------------------------------------------------------------------------- #
# Every Slicer version registers its own uninstall entry and installs into its own
# directory, so versions coexist. Re-installing a version that is already there,
# though, overwrites it without a word — so look for that case before downloading.
#
# Property access goes through PSObject.Properties because Set-StrictMode turns a
# missing registry value into a terminating error rather than $null.
function Get-InstalledSlicer {
    $keys = @(
        'HKCU:\Software\Microsoft\Windows\CurrentVersion\Uninstall\*',
        'HKLM:\Software\Microsoft\Windows\CurrentVersion\Uninstall\*',
        'HKLM:\Software\WOW6432Node\Microsoft\Windows\CurrentVersion\Uninstall\*'
    )
    Get-ItemProperty $keys -ErrorAction SilentlyContinue |
        Where-Object { $_.PSObject.Properties['DisplayName'] -and $_.DisplayName -like '*Slicer*' } |
        ForEach-Object {
            [pscustomobject]@{
                Name                 = [string]$_.DisplayName
                Version              = if ($_.PSObject.Properties['DisplayVersion'])       { [string]$_.DisplayVersion }       else { '' }
                Location             = if ($_.PSObject.Properties['InstallLocation'])      { [string]$_.InstallLocation }      else { '' }
                UninstallString      = if ($_.PSObject.Properties['UninstallString'])      { [string]$_.UninstallString }      else { '' }
                QuietUninstallString = if ($_.PSObject.Properties['QuietUninstallString']) { [string]$_.QuietUninstallString } else { '' }
            }
        }
}

# The directory a given version occupies, found by looking where the installer
# puts things. Backs up the registry, which may omit InstallLocation.
#
# The name must *end* with the version, never merely contain it: while 5.12.0 is
# unreleased its nightlies are named "Slicer 5.12.0-2026-01-01", and those are a
# different build that we must not offer to overwrite.
function Find-SlicerInstallDir {
    param([string]$Version)
    if (-not $Version) { return '' }
    foreach ($root in @((Join-Path $env:LOCALAPPDATA 'NA-MIC'), (Join-Path ${env:ProgramFiles} 'NA-MIC'))) {
        if ($root -and (Test-Path $root)) {
            $dir = Get-ChildItem -Path $root -Directory -ErrorAction SilentlyContinue |
                   Where-Object { $_.Name -like "*$Version" } | Select-Object -First 1
            if ($dir) { return $dir.FullName }
        }
    }
    return ''
}

function Find-SameVersionInstall {
    param([string]$Version)
    if (-not $Version) { return $null }

    # DisplayVersion decides whenever the registry records one ("5.12.0", or
    # "5.12.0.34621" where the build number is tacked on). Only when it is absent
    # do we fall back to reading the version off the display name.
    $match = Get-InstalledSlicer | Where-Object {
        $_.Version -eq $Version -or
        $_.Version -like "$Version.*" -or
        ((-not $_.Version) -and $_.Name -like "*$Version")
    } | Select-Object -First 1

    $dir = Find-SlicerInstallDir -Version $Version
    if ($match) {
        if (-not $match.Location -and $dir) { $match.Location = $dir }
        return $match
    }
    # Installed without (or with a stale) registry entry: the directory decides.
    if ($dir) { return [pscustomobject]@{ Name = "Slicer $Version"; Version = $Version; Location = $dir } }
    return $null
}

# Ask for a directory to install into. NSIS reads everything after /D= literally,
# to the end of the line, so the path must be ASCII and cannot be quoted.
function Read-InstallDir {
    param([string]$Forbidden)
    while ($true) {
        $dir = (Read-Host 'Install directory').Trim().Trim('"')
        if (-not $dir) {
            Write-Host '  Please enter a path.'
        } elseif ($dir -match '[^\x20-\x7E]') {
            Write-Host '  The path must contain only ASCII characters.'
        } elseif ($Forbidden -and ($dir.TrimEnd('\') -eq $Forbidden.TrimEnd('\'))) {
            Write-Host '  That is the existing installation. Pick another directory.'
        } else {
            try {
                New-Item -ItemType Directory -Force -Path $dir -ErrorAction Stop | Out-Null
                return $dir
            } catch {
                Write-Host "  Cannot create $dir. Pick another directory."
            }
        }
    }
}

# Run the registered uninstaller for an existing install and wait for it to
# finish. Slicer's uninstaller is NSIS-based, so it takes /S for a silent run;
# QuietUninstallString, when the registry records one, already includes it.
#
# An NSIS uninstaller normally copies itself to %TEMP% and relaunches so it can
# delete its own directory, returning to the caller before the work is done --
# which would defeat -Wait. Passing _?=<dir> disables that copy and makes the run
# synchronous, but then the uninstaller cannot delete itself. So we make the copy
# ourselves: running our own temp copy with _?=<installdir> both waits AND lets
# the original directory (uninstaller included) be removed cleanly.
function Uninstall-Slicer {
    param($Existing)

    # Prefer the command the registry records; fall back to the Uninstall.exe that
    # NSIS drops in the install directory.
    $cmd = ''
    if ($Existing.PSObject.Properties['QuietUninstallString'] -and $Existing.QuietUninstallString) {
        $cmd = $Existing.QuietUninstallString
    } elseif ($Existing.PSObject.Properties['UninstallString'] -and $Existing.UninstallString) {
        $cmd = $Existing.UninstallString
    }

    $exe = ''
    if ($cmd -match '^\s*"([^"]+)"') { $exe = $Matches[1] }
    elseif ($cmd -match '^\s*(\S+)') { $exe = $Matches[1] }

    $location = if ($Existing.PSObject.Properties['Location']) { [string]$Existing.Location } else { '' }
    if ((-not $exe) -and $location) {
        $candidate = Join-Path $location 'Uninstall.exe'
        if (Test-Path $candidate) { $exe = $candidate }
    }
    if ((-not $exe) -or (-not (Test-Path $exe))) {
        throw "Could not find an uninstaller for $($Existing.Name)."
    }

    Write-Step "Uninstalling $($Existing.Name)..."
    if ($location) {
        $temp = Join-Path $env:TEMP ("SlicerUninstall-" + [guid]::NewGuid().ToString('N') + ".exe")
        Copy-Item -LiteralPath $exe -Destination $temp -Force
        try {
            $proc = Start-Process -FilePath $temp `
                -ArgumentList "/S _?=$($location.TrimEnd('\'))" -Wait -PassThru
        } finally {
            Remove-Item $temp -Force -ErrorAction SilentlyContinue
        }
    } else {
        # No known directory to run in place from: launch the uninstaller as-is.
        $proc = Start-Process -FilePath $exe -ArgumentList '/S' -Wait -PassThru
    }

    if ($proc.ExitCode -ne 0) {
        throw "Uninstaller exited with code $($proc.ExitCode)."
    }
}

# Decide what to do about an installation of the version we are about to install.
# Returns the directory to install into, or $null to leave the target unchanged
# (which lets the installer fall back to its own default). Reinstalling returns
# the directory the existing copy occupies, so that a copy installed somewhere
# unusual is replaced where it stands rather than cloned into the default spot.
# SLICER_INSTALL_DIR, when set, has already chosen the target for this run.
# Never returns when the user aborts.
function Resolve-ExistingInstall {
    param($Existing, [string]$Version)

    $where = if ($Existing.Location) { $Existing.Location } else { '(location unknown)' }
    $inPlace = if ($env:SLICER_INSTALL_DIR) { $null } else { $Existing.Location }

    switch ($onExisting) {
        'abort' {
            Write-Step "3D Slicer $Version is already installed at $where; nothing to do."
            exit 0
        }
        'reinstall' {
            Write-Note "3D Slicer $Version is already installed at $where; reinstalling."
            return $inPlace
        }
        'uninstall' {
            Write-Step "3D Slicer $Version is already installed at $where; uninstalling it."
            Uninstall-Slicer -Existing $Existing
            Write-Done "3D Slicer has been uninstalled."
            exit 0
        }
    }

    # Asked not to prompt (automation/CI): reinstall in place, matching the
    # no-console fallback below. abort/reinstall/uninstall were handled above.
    if ($env:SLICER_NONINTERACTIVE) {
        Write-Note "3D Slicer $Version is already installed at $where; reinstalling (non-interactive mode)."
        return $inPlace
    }

    # No console to ask on (CI, a scheduled task, redirected input): reinstalling
    # is what this script has always done, so keep doing it rather than hang.
    if (-not [Environment]::UserInteractive -or [Console]::IsInputRedirected) {
        Write-Warning "3D Slicer $Version is already installed at $where, and there is no"
        Write-Warning "terminal to ask what to do. Reinstalling. Set SLICER_IF_EXISTING=abort to skip."
        return $inPlace
    }

    Write-Host ''
    Write-Warning "3D Slicer $Version is already installed at:"
    Write-Host "    $where"
    Write-Host ''
    Write-Host '  1) Abort - leave the existing installation untouched'
    Write-Host '  2) Reinstall - replace it with a freshly downloaded copy'
    Write-Host '  3) Install elsewhere - keep both, in a directory you choose'
    Write-Host '  4) Uninstall - remove the existing installation and exit'
    Write-Host ''

    while ($true) {
        $choice = (Read-Host 'Choose [1-4] (default 1)').Trim()
        switch ($choice) {
            { $_ -in @('', '1') } {
                Write-Step "Aborted; $where was left untouched."
                exit 0
            }
            '2' {
                $dest = if ($inPlace) { $inPlace } else { $env:SLICER_INSTALL_DIR }
                if ($dest) { Write-Step "Reinstalling into $dest." } else { Write-Step 'Reinstalling.' }
                return $inPlace
            }
            '3' {
                $dir = Read-InstallDir -Forbidden $Existing.Location
                Write-Note "Both copies will remain; Add/Remove Programs will list only this one."
                return $dir
            }
            '4' {
                Uninstall-Slicer -Existing $Existing
                Write-Done "3D Slicer has been uninstalled."
                exit 0
            }
            default { Write-Host '  Please answer 1, 2, 3 or 4.' }
        }
    }
}

# Download a URL to a file while showing a live progress bar. We stream the body
# ourselves in large chunks instead of using `Invoke-WebRequest -OutFile`: its
# built-in progress bar throttles the ~250 MB transfer to a crawl in Windows
# PowerShell, whereas streaming stays fast AND keeps the user informed.
function Save-UrlWithProgress {
    param(
        [Parameter(Mandatory)] [string]$Url,
        [Parameter(Mandatory)] [string]$OutFile,
        [string]$Activity = 'Downloading 3D Slicer',
        [int]$MaxAttempts = 3,
        [int]$RetryDelaySeconds = 2
    )

    # Draw the live bar only when stdout is attached to a console and quiet mode is
    # off. When output is redirected to a file or pipe, or SLICER_QUIET is set, stay
    # silent so the log stays clean, mirroring the `[ -t 2 ]` guard on curl/wget in
    # install.sh.
    $showProgress = (-not [Console]::IsOutputRedirected) -and (-not $env:SLICER_QUIET)

    # Retry a failed transfer a few times, mirroring curl's
    # --retry 3 --retry-delay 2 in install.sh, so a transient network hiccup does
    # not abort the whole install. Each attempt reopens the request and truncates
    # the output file, so a partial download from a failed try is never kept.
    for ($attempt = 1; ; $attempt++) {
        $req = [Net.HttpWebRequest]::Create($Url)
        $req.AllowAutoRedirect = $true
        $req.UserAgent = 'slicer-installer (PowerShell)'

        # Assigned inside the try; nulled here so the finally can dispose only what
        # was actually opened (Set-StrictMode forbids referencing unset vars).
        $resp = $null; $stream = $null; $file = $null
        try {
            $resp   = $req.GetResponse()
            $total  = [int64]$resp.ContentLength
            $stream = $resp.GetResponseStream()
            $file   = [IO.File]::Create($OutFile)

            $buffer   = [byte[]]::new(1MB)
            $read     = [int64]0
            $lastTick = 0
            while (($n = $stream.Read($buffer, 0, $buffer.Length)) -gt 0) {
                $file.Write($buffer, 0, $n)
                $read += $n
                # Refresh the bar at most ~10x/sec so Write-Progress never becomes the
                # bottleneck on a fast connection.
                $now = [Environment]::TickCount
                if ($showProgress -and $now - $lastTick -ge 100) {
                    $lastTick = $now
                    if ($total -gt 0) {
                        Write-Progress -Activity $Activity `
                            -Status ("{0:N1} / {1:N1} MB" -f ($read / 1MB), ($total / 1MB)) `
                            -PercentComplete ([int](($read * 100) / $total))
                    } else {
                        Write-Progress -Activity $Activity `
                            -Status ("{0:N1} MB" -f ($read / 1MB))
                    }
                }
            }
            return
        } catch {
            if ($attempt -ge $MaxAttempts) { throw }
            Write-Warning "Download attempt $attempt failed: $($_.Exception.Message). Retrying in $RetryDelaySeconds s..."
            Start-Sleep -Seconds $RetryDelaySeconds
        } finally {
            if ($showProgress) { Write-Progress -Activity $Activity -Completed }
            if ($file)   { $file.Dispose() }
            if ($stream) { $stream.Dispose() }
            if ($resp)   { $resp.Dispose() }
        }
    }
}

# Assigned inside the try; declared here so the finally block can reference them
# safely even if an early statement throws (Set-StrictMode forbids unset vars).
$installer     = $null
$keepInstaller = $false

try {
    $itemId   = Resolve-SlicerItemId -Url $downloadUrl
    $meta     = if ($itemId) { Get-SlicerMeta -ItemId $itemId } else { $null }
    $expected = if ($meta) { $meta.Sha512 } else { $null }
    $version  = if ($meta) { $meta.Version } else { $null }
    $source   = if ($itemId) { "$packagesApi/item/$itemId/download" } else { $downloadUrl }

    # $null means "let the installer pick its own directory".
    $targetDir = if ($env:SLICER_INSTALL_DIR) { $env:SLICER_INSTALL_DIR } else { $null }
    if ($targetDir -and $targetDir -match '[^\x20-\x7E]') {
        throw "SLICER_INSTALL_DIR must contain only ASCII characters: $targetDir"
    }

    # Settle what to do about an existing copy of this version before spending a
    # ~250 MB download on it. Without server metadata there is no version to
    # compare against, so we cannot tell, and carry on as before.
    $existing = Find-SameVersionInstall -Version $version
    if ($existing) {
        $chosen = Resolve-ExistingInstall -Existing $existing -Version $version
        if ($chosen) { $targetDir = $chosen }
    } else {
        $others = @(Get-InstalledSlicer)
        if ($others.Count -gt 0) {
            Write-Note "Already installed: $($others.Name -join ', '). Versions install side by side."
        }
    }

    # Cache verified installers between runs, keyed by version, so re-running the
    # script reuses an installer we already downloaded instead of fetching the
    # whole ~250 MB package again. When the version is unknown we can't name the
    # cache file stably, so we fall back to a throwaway temp file (no caching).
    $cacheDir = $null
    if ($version) {
        $cacheDir = Join-Path $env:LOCALAPPDATA 'slicer-installer'
        try { New-Item -ItemType Directory -Force -Path $cacheDir | Out-Null } catch { $cacheDir = $null }
    }
    if ($cacheDir -and (Test-Path $cacheDir)) {
        $installer     = Join-Path $cacheDir "Slicer-$version.exe"
        $keepInstaller = $true
    } else {
        $installer     = Join-Path $env:TEMP ("Slicer-" + [guid]::NewGuid().ToString('N') + ".exe")
        $keepInstaller = $false
    }

    # Reuse the cached installer when it is present and still matches the
    # publisher's checksum; without a checksum we can't trust it, so re-download.
    $cached = $false
    if ($keepInstaller -and $expected -and (Test-Path $installer)) {
        $have = (Get-FileHash -Path $installer -Algorithm SHA512).Hash.ToLower()
        if ($have -eq $expected.ToLower()) {
            Write-Step "Using cached 3D Slicer $version."
            $cached = $true
        }
    }

    if (-not $cached) {
        $label = if ($version) { "3D Slicer $version" } else { '3D Slicer' }
        Write-Step "Downloading $label for Windows..."
        Save-UrlWithProgress -Url $source -OutFile $installer -Activity "Downloading $label"

        if ($expected) {
            $actual = (Get-FileHash -Path $installer -Algorithm SHA512).Hash.ToLower()
            if ($actual -ne $expected.ToLower()) {
                throw "Checksum verification failed (expected $expected, got $actual)."
            }
            Write-Step "Checksum verified (SHA-512)."
        } else {
            Write-Warning "Could not obtain a checksum from the server; skipping verification."
        }
    }

    # The Slicer installer is NSIS-based:
    #   /S           silent install
    #   /D=<path>    install directory (must be the LAST argument, unquoted)
    # NSIS reads everything after /D= as the path, to the end of the command line,
    # so a path with spaces needs no quoting -- and must not get any. Spelling the
    # command line out as one string says that, rather than leaving it to how
    # Start-Process happens to join an argument array.
    $arguments = '/S'
    if ($targetDir) { $arguments += " /D=$targetDir" }

    Write-Step "Running the installer silently..."
    $proc = Start-Process -FilePath $installer -ArgumentList $arguments -Wait -PassThru
    if ($proc.ExitCode -ne 0) {
        throw "Installer exited with code $($proc.ExitCode)."
    }

    # Locate the installed launcher so we can tell the user where it went.
    $searchRoots = @()
    if ($targetDir) { $searchRoots += $targetDir }
    $searchRoots += (Join-Path $env:LOCALAPPDATA 'NA-MIC')
    $searchRoots += (Join-Path ${env:ProgramFiles} 'NA-MIC')

    $exe = $null
    foreach ($root in $searchRoots) {
        if ($root -and (Test-Path $root)) {
            $exe = Get-ChildItem -Path $root -Recurse -Filter 'Slicer.exe' -ErrorAction SilentlyContinue |
                   Select-Object -First 1
            if ($exe) { break }
        }
    }

    if ($exe) {
        Write-Step "Installed to: $($exe.FullName)"
    } else {
        Write-Step "Installation finished. Launch 3D Slicer from the Start menu."
    }
    Write-Done "3D Slicer installation complete."
}
finally {
    # Keep cached installers for the next run; only clean up throwaway temp ones.
    if (-not $keepInstaller -and $installer -and (Test-Path $installer)) {
        Remove-Item $installer -Force -ErrorAction SilentlyContinue
    }
}
